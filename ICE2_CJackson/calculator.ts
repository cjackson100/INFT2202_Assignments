export let add = (a: number, b: number) => {
  return a + b;
}

export let subtract = (a: number, b:number) => {
  return a - b;
}

export let multiply = (a: number, b: number) => {
  return a * b;
}

export let divide = (a: number, b: number) => {
  return a / b;
}
